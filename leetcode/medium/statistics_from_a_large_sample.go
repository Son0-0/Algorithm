func sampleStats(count []int) []float64 {
	result := make([]float64, 5)

	min, max := 256, 0
	sum, length, mode := 0, 0, 0

	for idx, num := range count {
		if num == 0 {
			continue
		}

		if idx < min {
			min = idx
			result[0] = float64(idx)
		}

		if max < idx {
			max = idx
			result[1] = float64(idx)
		}

		if mode < num {
			mode = num
			result[4] = float64(idx)
		}

		sum += num * idx
		length += num
	}

	result[2] = float64(sum) / float64(length)

	half := length / 2
	temp := 0

	if length%2 == 1 {
		for idx, num := range count {
			temp += num

			if half+1 <= temp {
				result[3] = float64(idx)
				return result
			}
		}
	} else {
		cnt := 0

		for idx, num := range count {
			temp += num

			if half <= temp {
				cnt -= 1
				if half+1 <= temp {
					result[3] = float64(idx)
					return result
				} else {
					for j := idx + 1; j < 256; j++ {
						if count[j] != 0 {
							result[3] = float64(idx+j) / 2
							return result
						}
					}
				}
			}
		}
	}

	return result
}

