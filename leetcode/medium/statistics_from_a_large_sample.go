func sampleStats(count []int) []float64 {
	result := make([]float64, 5)

	min, max := 256, 0
	sum, length, mode := 0, 0, 0

	for idx, num := range count {
		if num == 0 {
			continue
		}

		if idx < min {
			min = idx
			result[0] = float64(idx)
		}

		if max < idx {
			max = idx
			result[1] = float64(idx)
		}

		if mode < num {
			mode = num
			result[4] = float64(idx)
		}

		sum += num * idx
		length += num
	}

	result[2] = float64(sum) / float64(length)

	half := length / 2
	temp := 0

	if length%2 == 1 {
		for idx, num := range count {
			temp += num

			if half+1 <= temp {
				result[3] = float64(idx)
				return result
			}
		}
	} else {
		left, right := 0, 0

		for idx, num := range count {
			temp += num

			if half+1 <= temp {
				left = idx
				right = idx
				break
			} else if temp == half {
				left = idx
				for k := idx + 1; k < length; k++ {
					if count[k] != 0 {
						right = k
						break
					}
				}
				break
			}
		}
		result[3] = float64(left+right) / 2
		return result
	}

	// result[3] = float64(left+right) / 2

	return result
}

